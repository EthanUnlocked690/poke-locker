let currentPose = "stand";

// Updated changePose function that uses animated GIFs from the Python script
function changePose(poseId) {
  // Convert "pose3" → "3" or keep "stand" as is
  let poseName = poseId.replace('pose', '');
  if (poseName === "1" || poseName === "") poseName = "stand";

  currentPose = poseName;

  const poseImage = document.getElementById('poseImage');
  
  // Try animated GIF first (generated by your animations.py)
  const animatedSrc = `./animations/output/trainer_${poseName}.gif`;
  
  // Fallback chain: animated GIF → static pose image
  const img = new Image();
  img.onload = () => {
    poseImage.src = animatedSrc;   // Use animated version if it exists
  };
  img.onerror = () => {
    poseImage.src = `./images/poses/${poseName}.png`;  // fallback to static
  };
  img.src = animatedSrc;  // trigger the check
}

// Updated loadCosmetics to handle poses with animation
function loadCosmetics(category) {
  const scroller = document.getElementById('cosmeticsScroller');
  const title = document.getElementById('categoryTitle');
  scroller.innerHTML = '';

  title.textContent = category === 'poses' 
    ? "POSES - Click to change your trainer's animated pose" 
    : `${category.toUpperCase()} - Click any item to equip`;

  const items = cosmeticsDB[category] || [];

  items.forEach(item => {
    const div = document.createElement('div');
    div.className = 'cosmetic-item';
    div.dataset.id = item.id;
    div.innerHTML = `
      <div class="item-thumb">
        <img src="./images/cosmetics/${category}/${item.id}.png" 
             alt="${item.name}" 
             onerror="this.src='./images/placeholder.png'; this.style.opacity='0.5';">
      </div>
      <p>${item.name}</p>
    `;

    div.addEventListener('click', () => {
      document.querySelectorAll('.cosmetic-item').forEach(el => el.classList.remove('selected'));
      div.classList.add('selected');

      if (item.isPose) {
        changePose(item.id);      // ← uses animated GIF
      } else {
        selectCosmetic(item);
      }
    });

    scroller.appendChild(div);
  });
}

// Optional: Update randomizeOutfit to also randomize pose with animation
function randomizeOutfit() {
  const clothingLayers = ['hat', 'shirt', 'pants', 'shoes', 'accessory'];
  clothingLayers.forEach(layer => {
    const cat = Object.keys(cosmeticsDB).find(c => {
      const sample = cosmeticsDB[c][0];
      return sample && sample.layer === layer;
    });
    if (cat) {
      const randomItem = cosmeticsDB[cat][Math.floor(Math.random() * cosmeticsDB[cat].length)];
      currentOutfit[layer] = randomItem.id;
    }
  });

  // Random animated pose
  const poseItems = cosmeticsDB.poses || [];
  if (poseItems.length > 0) {
    const randomPoseItem = poseItems[Math.floor(Math.random() * poseItems.length)];
    changePose(randomPoseItem.id);
  }

  updatePreview();
}

